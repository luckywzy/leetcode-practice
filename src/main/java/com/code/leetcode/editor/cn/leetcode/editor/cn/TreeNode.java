package com.code.leetcode.editor.cn.leetcode.editor.cn;

/**
 * Definition for a binary tree node.
 */
public class TreeNode {
    public int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
