class CacheNode {
    int key;
    int value;

    CacheNode prev;
    CacheNode next;

    CacheNode(int key, int value) {
        this.key = key;
        this.value = value;
    }

    CacheNode() {

    }
}

class LRUCache {
    HashMap<Integer, CacheNode> map;

    int capacity;
    int usedCapacity;

    CacheNode head;
    CacheNode tail;

    public LRUCache(int capacity) {
        map = new HashMap<Integer, CacheNode>();
        this.capacity = capacity;
        head = new CacheNode();
        tail = new CacheNode();
        head.next = tail;
        tail.prev = head;
    }

    public int get(int key) {
        if (map.containsKey(key)) {
            CacheNode newHead = map.get(key);
            //move
            remove(newHead);

            //to head
            addToHead(newHead);

            return newHead.value;
        }
        return -1;
    }

    public void put(int key, int value) {
        CacheNode newNode = new CacheNode(key, value);
        map.put(key, newNode);
        addToHead(newNode);
        usedCapacity++;

        while (usedCapacity > capacity) {
            CacheNode last = tail.prev;
            remove(last);
            map.remove(last.key);
            usedCapacity--;
        }
    }

    private void remove(CacheNode node) {
        CacheNode prev = node.prev;
        CacheNode next = node.next;
        prev.next = next;
        next.prev = prev;
    }

    private void addToHead(CacheNode newHead) {
        CacheNode oldHead = head.next;

        head.next = newHead;
        newHead.prev = head;
        newHead.next = oldHead;

        oldHead.prev = newHead;
    }

}

